public class Emp 
{
	String empNo;
	String ename;
	String job;
	String mgr;
	String hiredate;
	String sal;
	String comm;
	String deptNo;
	
	public Emp(){}
	
	public Emp(String empNo,String ename,String job,String mgr,String hireDate,String sal,String comm,String deptNo)
	{
		this.empNo=empNo;
		this.ename=ename;
		this.job=job;
		this.mgr=mgr;
		this.hiredate=hireDate;
		this.sal=sal;
		this.comm=comm;
		this.deptNo=deptNo;
	}
	
}